const int MOD = 1e9 + 7;
const int N = 1e6 + 5;

void solve(){
	int n; cin >> n;
	int a[n + 5];
	for (int i = 0; i < n; i++) cin >> a[i];

	int dp[n + 5];
	int max_e = INT_MIN;
	for (int i = 0; i < n; i++){
		dp[i] = 1;
		for (int j = 0; j < i; j++){
			if (a[j] <= a[i]){
				dp[i] = max(dp[i], dp[j] + 1);
				max_e = max(dp[i], max_e);
			}
		}
	}

	cout << n - max_e << EL;
}

int main(){
	faster();
	int TC; cin >> TC;
	while (TC--){
	    solve();
	}
	return 0;
}

